-- Drop table if exists (for clean re-runs)
DROP TABLE IF EXISTS fuel_consumption_benchmarks_historical;

-- Create the historical benchmarks table
CREATE TABLE fuel_consumption_benchmarks_historical (
    id SERIAL PRIMARY KEY,
    country_code VARCHAR(3) NOT NULL,
    country_name VARCHAR(100) NOT NULL,
    year INTEGER NOT NULL,
    suv_L100 DECIMAL(4,1) NOT NULL,      -- SUV/Light Truck fuel consumption (L/100km)
    car_L100 DECIMAL(4,1) NOT NULL,      -- Sedan/Passenger Car fuel consumption (L/100km)
    fleet_avg_L100 DECIMAL(4,1),         -- Overall fleet average if available
    region VARCHAR(50) NOT NULL,
    data_quality VARCHAR(20) DEFAULT 'estimated',  -- 'measured', 'estimated', 'interpolated'
    source_notes TEXT,
    UNIQUE(country_code, year)
);

-- Create indexes for efficient querying
CREATE INDEX idx_historical_country ON fuel_consumption_benchmarks_historical(country_code);
CREATE INDEX idx_historical_year ON fuel_consumption_benchmarks_historical(year);
CREATE INDEX idx_historical_region ON fuel_consumption_benchmarks_historical(region);

